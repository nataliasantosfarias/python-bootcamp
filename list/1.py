lista = ["p" , "y" ,"t" , "h" , "o" , "n"]

print(lista[:2]) # como se fosse 2 -1  ['p', 'y']

print(lista[2:]) # como se fosse 2 + 1  ['z', 'h', 'o', 'n']

print(lista[1:3]) # lê a lista de acordo com o parâmetro definido, posição 1 e 3 : ['y', 't']

print(lista[::]) # lê a lista vazia

print(lista[::-1]) # lê a lista ao contrário